        $('#csubmit').on('click', function() {
			var email="",name="",phone="",message="";
			name = $('#contact_name').val();
			email = $('#contact_email').val();
			phone = $('#contact_phone').val();
			message = $('#contact_message').val();
			var url ="api/contactUs";
			var requestData = {
								fromEmail:email,
								fromName : name,
								fromPhone : phone,
								fromMessage : message
							};
			if(email != "" && name != "" && message != ""){

			$.ajax({
				type: "POST",
				contentType: "application/json; charset=utf-8",
				dataType: "json",
				url: url,
				data: JSON.stringify(requestData),
				success : function(data){
					if(data.success){
						sweetAlert("Success","Thanks for contacting. We will get back to you soon.","success");
						name = $('#contact_name').val('');
						email = $('#contact_email').val('');
						phone = $('#contact_phone').val('');
						message = $('#contact_message').val('');
					}
			}
			}); 
			}else{
				sweetAlert("Error","You missed some fields.","error");
			}
        });
